export interface StatConfig {
  id: string;
  name: string;
  tooltip?: string;
  valueTooltip?: string;
  format?: string;
  showWhenExpanded?: boolean;
}

export interface StatReference {
  id: string;
  showWhenExpanded?: boolean;
}

export interface StatCategoryConfig {
  id: string;
  name: string;
  stats: StatReference[];
  isExpanded?: boolean;
}

export interface StatsConfiguration {
  categories: StatCategoryConfig[];
  version: number;
}

export interface AvailableStat {
  name: string;
  type: "percent" | "flat" | "attribute" | "fixed";
  category?: string;
}

// Helper function to resolve stat references to full stat configurations
export const resolveStatReference = (
  statRef: StatReference
): StatConfig | undefined => {
  const baseStat = ALL_AVAILABLE_STATS.find((stat) => stat.id === statRef.id);
  if (!baseStat) return undefined;

  return {
    ...baseStat,
    showWhenExpanded: statRef.showWhenExpanded ?? baseStat.showWhenExpanded,
  };
};

